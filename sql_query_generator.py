def detect_table(query_text):
    query_lower = query_text.lower()

    if "transaction" in query_lower or "payment" in query_lower or "amount" in query_lower:
        return "transactions"
    elif "product" in query_lower or "item" in query_lower or "price" in query_lower:
        return "products"
    else:
        return "users"

def generate_sql(query_text):
    if not is_safe(query_text):
        return None, "Unsafe SQL detected"

    query_lower = query_text.lower()
    table = detect_table(query_text)

    # Start building SQL
    sql = f"SELECT * FROM {table}"
    conditions = []

    # ===== AMOUNT CONDITIONS =====
    amount_patterns = [
        (r'> (\d+)', '>'),
        (r'greater than (\d+)', '>'),
        (r'< (\d+)', '<'),
        (r'less than (\d+)', '<'),
        (r'>= (\d+)', '>='),
        (r'<= (\d+)', '<=')
    ]

    for pattern, operator in amount_patterns:
        match = re.search(pattern, query_lower)
        if match:
            if table == "transactions":
                conditions.append(f"amount {operator} {match.group(1)}")
            elif table == "products":
                conditions.append(f"price {operator} {match.group(1)}")
            break

    # ===== TIME CONDITIONS =====
    if "last week" in query_lower or "past week" in query_lower:
        if table == "users":
            conditions.append("signup_date >= DATE('now', '-7 days')")
        elif table == "transactions":
            conditions.append("date >= DATE('now', '-7 days')")
    elif "last month" in query_lower or "past month" in query_lower:
        if table == "users":
            conditions.append("signup_date >= DATE('now', '-1 month')")
        elif table == "transactions":
            conditions.append("date >= DATE('now', '-1 month')")
    elif "last 30 days" in query_lower:
        if table == "users":
            conditions.append("signup_date >= DATE('now', '-30 days')")
        elif table == "transactions":
            conditions.append("date >= DATE('now', '-30 days')")
    elif "today" in query_lower:
        if table == "users":
            conditions.append("DATE(signup_date) = DATE('now')")
        elif table == "transactions":
            conditions.append("DATE(date) = DATE('now')")

    # ===== STATUS CONDITIONS =====
    if "active" in query_lower and table == "users":
        conditions.append("status = 'active'")
    elif "inactive" in query_lower and table == "users":
        conditions.append("status = 'inactive'")

    # ===== COUNTRY CONDITIONS =====
    countries = ["usa", "uk", "canada", "australia", "india", "germany", "france"]
    for country in countries:
        if country in query_lower and table == "users":
            conditions.append(f"country = '{country.upper()}'")
            break

    # ===== CATEGORY CONDITIONS =====
    categories = ["electronics", "clothing", "food", "books", "furniture"]
    for category in categories:
        if category in query_lower and (table == "products" or table == "transactions"):
            conditions.append(f"category = '{category}'")
            break

    # ===== AGE CONDITIONS =====
    age_match = re.search(r'age (\d+)', query_lower)
    if age_match and table == "users":
        conditions.append(f"age = {age_match.group(1)}")

    # ===== ORDER BY =====
    order_clause = ""
    if "latest" in query_lower or "recent" in query_lower or "newest" in query_lower:
        if table == "users":
            order_clause = " ORDER BY signup_date DESC"
        elif table == "transactions":
            order_clause = " ORDER BY date DESC"
    elif "oldest" in query_lower:
        if table == "users":
            order_clause = " ORDER BY signup_date ASC"
        elif table == "transactions":
            order_clause = " ORDER BY date ASC"
    elif "highest" in query_lower or "largest" in query_lower:
        if table == "transactions":
            order_clause = " ORDER BY amount DESC"
        elif table == "products":
            order_clause = " ORDER BY price DESC"
    elif "lowest" in query_lower or "smallest" in query_lower:
        if table == "transactions":
            order_clause = " ORDER BY amount ASC"
        elif table == "products":
            order_clause = " ORDER BY price ASC"

    # Build the final SQL
    if conditions:
        sql += " WHERE " + " AND ".join(conditions)

    sql += order_clause

    # ===== LIMIT =====
    limit_match = re.search(r'limit (\d+)', query_lower)
    if limit_match:
        sql += f" LIMIT {limit_match.group(1)}"
    else:
        # Check for implicit limits
        if "top 10" in query_lower or "first 10" in query_lower:
            sql += " LIMIT 10"
        elif "top 5" in query_lower or "first 5" in query_lower:
            sql += " LIMIT 5"
        elif "all" in query_lower:
            sql += " LIMIT 1000"
        else:
            sql += " LIMIT 50"  # Default limit

    return sql, f"SQL generated for {table} table"


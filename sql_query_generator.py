def download_txt():
    if not query_history:
        return "No queries", None

    filename = f"sql_queries_{datetime.now().strftime('%Y%m%d_%H%M%S')}.txt"
    content = f"SQL QUERY HISTORY\nGenerated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n{'='*50}\n\n"

    for i, q in enumerate(query_history, 1):
        content += f"Query #{i}\n{'-'*30}\n"
        content += f"User: {q['user']}\n"
        content += f"SQL: {q['sql']}\n"
        content += f"Time: {q['timestamp']}\n\n"

    Path(filename).write_text(content)
    return "TXT Generated", filename


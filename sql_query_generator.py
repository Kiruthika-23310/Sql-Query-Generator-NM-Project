def download_pdf():
    if not query_history:
        return "No queries", None

    filename = f"sql_queries_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
    doc = SimpleDocTemplate(filename, pagesize=letter)
    styles = getSampleStyleSheet()
    story = []

    story.append(Paragraph("SQL Query Report", styles["Heading1"]))
    story.append(Spacer(1, 20))
    story.append(Paragraph(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", styles["Normal"]))
    story.append(Spacer(1, 20))

    for i, q in enumerate(query_history, 1):
        story.append(Paragraph(f"Query #{i}", styles["Heading2"]))
        story.append(Paragraph(f"User: {q['user']}", styles["Normal"]))
        story.append(Paragraph(f"SQL: {q['sql']}", styles["Normal"]))
        story.append(Paragraph(f"Time: {q['timestamp']}", styles["Normal"]))
        story.append(Spacer(1, 10))

    doc.build(story)
    return "PDF Generated", filename


def process_query(user_query, chat_history):
    global query_history

    if not user_query or not user_query.strip():
        chat_history.append(("", "⚠️ Please enter a query"))
        return chat_history

    sql_query, message = generate_sql(user_query)

    if sql_query is None:
        response = "❌ " + message
    else:
        response = "✅ SQL GENERATED\n\n```sql\n" + sql_query + "\n```\n\n📝 " + message

        query_history.append({
            "user": user_query,
            "sql": sql_query,
            "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        })

    chat_history.append((user_query, response))
    return chat_history


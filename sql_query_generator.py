DANGEROUS_KEYWORDS = ["DROP","DELETE","ALTER","TRUNCATE","INSERT","UPDATE","CREATE","REPLACE"]
DANGEROUS_CHARS = [";","--","/*","*/"]

def is_safe(text):
    text_upper = text.upper()

    for keyword in DANGEROUS_KEYWORDS:
        if keyword in text_upper:
            return False

    for char in DANGEROUS_CHARS:
        if char in text:
            return False

    return True

